function lose () {
    if (info.life() == 0) {
        game.over(false)
    }
}
function Interact () {
    // Makes hero says something
    Rapper.say("I'll let you go first", 2000)
    // Creates a pause
    pause(2000)
    kanye.say("Wrong choice", 2000)
    pause(2000)
    kanye.say("Poopity scoop", 2000)
    pause(2000)
    // ends game
    info.changeLifeBy(-1)
}
